	public string GetLocalizedText(string key)
	{
		//if (AllLocalizationDictionaries.ContainsKey(currentLanguage))
		//{
		//    return AllLocalizationDictionaries[currentLanguage][key];
		//}
		return AllLocalizationDictionaries[SystemLanguage.English][key];
	}

	private void CheckLocalizationsIntegrity()
	{
		List<string> list = new List<string>();
		foreach (string key in AllLocalizationDictionaries[SystemLanguage.English].Keys)
		{
			foreach (KeyValuePair<SystemLanguage, Dictionary<string, string>> allLocalizationDictionary in AllLocalizationDictionaries)
			{
				if (!allLocalizationDictionary.Value.ContainsKey(key))
				{
					list.Add(allLocalizationDictionary.Key + " " + key);
				}
			}
		}
		List<string> list2 = new List<string>();
		int count = AllLocalizationDictionaries[SystemLanguage.English].Keys.Count;
		foreach (KeyValuePair<SystemLanguage, Dictionary<string, string>> allLocalizationDictionary2 in AllLocalizationDictionaries)
		{
			if (allLocalizationDictionary2.Value.Keys.Count != count)
			{
				list2.Add(allLocalizationDictionary2.Key.ToString());
			}
		}
		if (list.Count > 0)
		{
			throw new Exception("Localization exception. Missing items in some language(s): " + string.Join(", ", list.ToArray()));
		}
		if (list2.Count > 0)
		{
			throw new Exception("Localization exception. Inconsistent items count for some language(s): " + string.Join(", ", list2.ToArray()));
		}
	}

	public string GetCoinsNumberEnding(int num)
	{
		return (num == 1) ? GetLocalizedText("CoinsSingle") : ((num % 10 == 1 && num % 100 != 11) ? GetLocalizedText("CoinsNom") : ((num % 10 < 2 || num % 10 > 4 || (num % 100 >= 10 && num % 100 < 20)) ? GetLocalizedText("CoinsPlu") : GetLocalizedText("CoinsGen")));
